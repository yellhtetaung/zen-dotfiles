#!/usr/bin/bash
# Custom Alias and Environment Variables for Zsh

# Aliases for showing hidden files and colored output
alias ls='ls -G'
alias la='ls -aG'
alias ll='ls -lG'
alias lsa='ls -lAG'

# Navigation Aliases
alias ..='cd ..'
alias ...='cd ../..'

# Common Commands
alias reload='source $HOME/.zprofile'
